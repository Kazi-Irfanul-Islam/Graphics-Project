//      
void applySmoothGrassColor(float y) {
    //       
    float minY = 0.0f;    //    (  )
    float maxY = 400.0f;  //   ( E72)

    //          (0.0  1.0  )
    float ratio = (y - minY) / (maxY - minY);

    //   0-   1-   
    if (ratio < 0.0f) ratio = 0.0f;
    if (ratio > 1.0f) ratio = 1.0f;

    // ---    (   ) ---

    //    (Deep Shadow Green)
    float lowR = 0.30f, lowG = 0.45f, lowB = 0.25f;
    //    (Bright Sunny Green - slightly lighter)
    float highR = 0.85f, highG = 0.92f, highB = 0.55f;

    //   (Linear Interpolation) -    
    float r = lowR + ratio * (highR - lowR);
    float g = lowG + ratio * (highG - lowG);
    float b = lowB + ratio * (highB - lowB);

    //    
    glColor3f(r, g, b);
}

